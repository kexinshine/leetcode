# @lc code=start
class Solution:
    def minCut(self, s: str) -> int:
        n = len(s)
        f = [[True] * n for _ in range(n)]

        for i in range(n - 1, -1, -1):
            for j in range(i + 1, n):
                f[i][j] = (s[i] == s[j]) and f[i + 1][j - 1]

        self.res=float('inf')
        ans = list()

        def dfs(i: int):
            if i == n:
                self.res=min(len(ans),self.res)
                return
            
            for j in range(i, n):
                if f[i][j]:
                    ans.append(s[i:j+1])
                    dfs(j + 1)
                    ans.pop()

        dfs(0)
        if self.res:
            return self.res-1
        return 0
# @lc code=end

