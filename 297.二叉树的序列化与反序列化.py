class Codec:
    def __init__(self):
        self.ans=[]

    def serialize(self, root):
        """Encodes a tree to a single string.
        
        :type root: TreeNode
        :rtype: str
        """
        if not root:
            return ""
        queue = collections.deque([root])
        res = []
        while queue:
            node = queue.popleft()
            if node:
                res.append(str(node.val))
                queue.append(node.left)
                queue.append(node.right)
            else:
                res.append('None')
        return '[' + ','.join(res) + ']'


    def deserialize(self, data):
        """Decodes your encoded data to tree.
        
        :type data: str
        :rtype: TreeNode
        """
        if not data:
            return 
        data = data[1:-1].split(',')
        root=TreeNode(data[0])
        queue=collections.deque([root])
        i=1
        while queue:
            node=queue.popleft()
            if data[i]!='None':
                node.left=TreeNode(data[i])
                queue.append(node.left)
            i+=1
            if data[i]!='None':
                node.right=TreeNode(data[i])
                queue.append(node.right)
            i+=1
        return root
        


